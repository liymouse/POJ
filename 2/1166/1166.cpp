#include <stdio.h>

int main()
{
int p[9][9] = {{-1 , 2 , -1 , 2 , 2 , -3 , -1 , -3 , 12},
			{3 , -1 , 3 , -1 , -1 , -1 , -2 , 4 , -6},
			{-1 , 2 , -1 , -3 , 2 , 2 , 4 , -3 , 7},
			{3 , -1 , -2 , -1 , -1 , 4 , 3 , -1 , -6},
			{-1 , 2 , -1 , 2 , -3 , 2 , -1 , 2 , -5},
			{-2 , -1 , 3 , 4 , -1 , -1 , -2 , -1 , -1},
			{-1 , -3 , 4 , 2 , 2 , -3 , -1 , 2 , -1},
			{-2 , 4 , -2 , -1 , -1 , -1 , 3 , -1 , 7},
			{4 , -3 , -1 , -3 , 2 , 2 , -1 , 2 , -1}};
int a[9] , i , j , m[9] = {0};

	for (i = 0; i < 9; i ++) scanf("%d" , &a[i]);
	for (i = 0; i < 9; i ++)
	{
		m[i] = 0;
		for (j = 0; j < 9; j ++)
			m[i] += p[i][j] * (-a[j]);
		while (m[i] > 0) m[i] -= 4;
		while (m[i] < 0) m[i] += 4;
	}
	for (i = 0; i < 9; i ++) while (m[i] --) printf("%d " , i + 1);
	return 0;
}